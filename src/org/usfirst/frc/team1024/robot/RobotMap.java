package org.usfirst.frc.team1024.robot;

/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {
	public static final int FRONT_LEFT_PORT = 1;
	public static final int FRONT_RIGHT_PORT = 2;
	public static final int BACK_LEFT_PORT = 3;
	public static final int BACK_RIGHT_PORT = 4;
	
	public static final int LOGITECH_PORT = 0;
	public static final int GYRO_PORT = 0;
}
